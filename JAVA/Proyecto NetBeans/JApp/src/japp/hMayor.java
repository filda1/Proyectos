
package japp;

public class hMayor {
    
    private int hourMayor ;
    private int minMayor ;
    private int segMayor ;
    private int hourMenor ;
    private int minMenor ;
    private int segMenor ;

    public hMayor(int hourMayor, int minMayor, int segMayor, int hourMenor, int minMenor, int segMenor) {
        this.hourMayor = hourMayor;
        this.minMayor = minMayor;
        this.segMayor = segMayor;
        this.hourMenor = hourMenor;
        this.minMenor = minMenor;
        this.segMenor = segMenor;
    }

    public int getHourMayor() {
        return hourMayor;
    }

    public int getMinMayor() {
        return minMayor;
    }

    public int getSegMayor() {
        return segMayor;
    }

    public int getHourMenor() {
        return hourMenor;
    }

    public void setHourMayor(int hourMayor) {
        this.hourMayor = hourMayor;
    }

    public void setMinMayor(int minMayor) {
        this.minMayor = minMayor;
    }

    public void setSegMayor(int segMayor) {
        this.segMayor = segMayor;
    }

    public void setHourMenor(int hourMenor) {
        this.hourMenor = hourMenor;
    }

    public void setMinMenor(int minMenor) {
        this.minMenor = minMenor;
    }

    public void setSegMenor(int segMenor) {
        this.segMenor = segMenor;
    }

    public int getMinMenor() {
        return minMenor;
    }

    public int getSegMenor() {
        return segMenor;
    }
}
